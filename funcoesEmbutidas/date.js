let now = new Date()
console.log(now)
console.log(now.getDate()) // pega dia do mês 1 - 31
console.log(now.getDay()) // pega dia da semana 0 - 6
console.log(now.getMonth()) // pega o mês 0 - 11
console.log(now.getFullYear()) // pega o ano yyyy
console.log(now.getHours()) // pega a hora 0 - 23
console.log(now.getMinutes()) // pega os minutos 0 - 59
console.log(now.getSeconds()) // pega os segundos 0 - 59
