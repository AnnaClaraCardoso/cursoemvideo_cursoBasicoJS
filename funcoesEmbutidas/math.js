console.log(Math.round(3.7)) // retorna o valor arredondado, 4
console.log(Math.abs(-3)) // retorna o valor positivo, 3
console.log(Math.sqrt(81)) // retorna a raíz quadrada, 9
console.log(Math.ceil(5.2)) // retorna o valor arredondado para cima, 6
console.log(Math.floor(4.9)) // retorna o valor arredondado para baixo, 4
console.log(Math.PI) // retorna o valor da constante PI 3.14
console.log(Math.random()) // retorna um número randômico entre 0 e 1
console.log(Math.pow(6,2)) // retorna o primeiro valor elevado à potência do segundo valor, 36
console.log(Math.min(6, 2, 9)) //retorna o menor valor, 2
console.log(Math.max(6, 2, 9)) //retorna o maior valor, 9